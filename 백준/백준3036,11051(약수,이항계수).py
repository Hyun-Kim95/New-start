n = int(input())                                    # 3036 첫번째 링이 한바퀴 돌 때, 나머지 링들의 돌은 바퀴수
r = list(map(int,input().split()))                  # 링들의 반지름들
for i in range(1,n):
    if r[0] % r[i] == 0:                            # 첫번째 링의 반지름의 약수일때
        print(r[0]//r[i],"/",1,sep="")              # 4/1 이런식으로 출력
    else:
        for j in range(r[i],0,-1):                  # 아닐경우 공약수 찾아서 출력
            if r[0] % j == 0 and r[i] % j == 0:
                print(r[0]//j,"/",r[i]//j,sep="")
                break

